/**
 * Script to update hardcoded benchmark data
 * Run: ARTIFICIAL_ANALYSIS_API_KEY=xxx node --import tsx scripts/update-benchmarks.ts
 *
 * This fetches fresh data from Artificial Analysis API and updates
 * provider-failover/benchmarks-chunk-*.ts files.
 *
 * Also auto-updates provider-failover/hardcoded-benchmarks.ts to import
 * the correct number of chunk files.
 */

import { readdirSync, readFileSync, unlinkSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const API_KEY = process.env.ARTIFICIAL_ANALYSIS_API_KEY;

if (!API_KEY) {
	console.error(
		"❌ Error: ARTIFICIAL_ANALYSIS_API_KEY environment variable required",
	);
	console.error("Get a free key at: https://artificialanalysis.ai");
	process.exit(1);
}

const CHUNK_SIZE = 90; // Models per chunk file (~2000 lines each)
const OUTPUT_DIR = join(process.cwd(), "provider-failover");

interface AAModel {
	id: string;
	name: string;
	slug: string;
	model_creator: {
		id: string;
		name: string;
		slug: string;
	};
	release_date: string;
	evaluations: {
		artificial_analysis_intelligence_index: number | null;
		artificial_analysis_coding_index: number | null;
		artificial_analysis_math_index: number | null;
		mmlu_pro: number | null;
		gpqa: number | null;
		hle: number | null;
	};
	context_window?: number;
	supports_reasoning?: boolean;
	supports_vision?: boolean;
}

async function fetchAIData(): Promise<AAModel[]> {
	console.log("📡 Fetching data from Artificial Analysis API...");

	const response = await fetch(
		"https://artificialanalysis.ai/api/v2/data/llms/models",
		{
			headers: {
				"x-api-key": API_KEY,
				Accept: "application/json",
			},
		},
	);

	if (!response.ok) {
		throw new Error(`API error: ${response.status} ${response.statusText}`);
	}

	const rawData = (await response.json()) as unknown;

	// API returns { data: [...] } or direct array
	let models: AAModel[];
	if (Array.isArray(rawData)) {
		models = rawData as AAModel[];
	} else if (rawData && typeof rawData === "object") {
		const obj = rawData as Record<string, unknown>;
		models = (obj.data || obj.models || []) as AAModel[];
	} else {
		models = [];
	}

	if (!Array.isArray(models) || models.length === 0) {
		console.error("Unexpected API response structure");
		throw new Error("API response did not contain models array");
	}

	return models;
}

function sanitizeString(s: string): string {
	// Strip CRLF characters to prevent log injection (SonarCloud S1075)
	return s.replaceAll(/[\n\r]/g, "_");
}

/**
 * Sanitize a value for safe console.log output.
 * Prevents log injection from external/untrusted data (SonarCloud S5693).
 */
function sanitizeForLog(value: unknown): string {
	const s = String(value);
	// Replace CR, LF, and tab characters that could forge log entries
	return s.replaceAll(/[\n\r\t]/g, " ");
}

function normalizeModelName(name: string): string {
	name = sanitizeString(name);
	name = name.toLowerCase().replace(/[^-a-z0-9.+]+/g, "-");
	// Strip leading/trailing dashes (no regex — avoids backtracking flags)
	while (name.startsWith("-")) name = name.slice(1);
	while (name.endsWith("-")) name = name.slice(0, -1);
	return name;
}

function generateChunkFile(
	chunkIndex: number,
	entries: string[],
	firstModel: string,
	lastModel: string,
	today: string,
): string {
	return `// Auto-generated benchmark data chunk ${chunkIndex}
// Models: ${firstModel} .. ${lastModel} (${entries.length} entries)
// Last updated: ${today}
// DO NOT EDIT MANUALLY — generated by scripts/update-benchmarks.ts

import type { HardcodedBenchmark } from "./hardcoded-benchmarks.ts";

export const BENCHMARKS_CHUNK_${chunkIndex}: Record<string, HardcodedBenchmark> = {
${entries.join("\n")}
};
`;
}

function generateBenchmarksChunks(models: AAModel[]): void {
	const today = new Date().toISOString().split("T")[0];

	// Filter to models with intelligence scores (allow 0, reject null/undefined)
	const scoredModels = models.filter(
		(m) => m.evaluations?.artificial_analysis_intelligence_index != null,
	);

	console.log(
		`✅ Found ${sanitizeForLog(scoredModels.length)}/${sanitizeForLog(models.length)} models with benchmark scores`,
	);

	// Generate entries
	const allEntries = scoredModels.map((model) => {
		const key = normalizeModelName(model.name);
		// Include original name for debugging name collisions
		const originalName = model.name.replaceAll(/[\n\r]/g, "_");
		const e = model.evaluations;

		// Helper to format optional number fields (strips trailing zeros to avoid S7748)
		const fmt = (v: number | null | undefined): string => {
			if (v === null || v === undefined) return "undefined";
			// parseFloat strips trailing zeros (avoids S7748 + S5852 regex backtracking)
			return String(Number(v.toFixed(3)));
		};

		return `	"${key}": {
		// AA specific benchmarks
		codingIndex: ${fmt(e.artificial_analysis_coding_index)},
		mathIndex: ${fmt(e.artificial_analysis_math_index)},

		// Academic benchmarks
		mmluPro: ${fmt(e.mmlu_pro)},
		gpqa: ${fmt(e.gpqa)},
		hle: ${fmt(e.hle)},

		// Capabilities
		contextWindow: ${model.context_window || 8192},
		supportsReasoning: ${model.supports_reasoning || false},
		supportsVision: ${model.supports_vision || false},

		// Metadata
		lastUpdated: "${today}",
		originalModel: "${originalName}",
	},`;
	});

	// Delete old chunk files
	const existingChunks = readdirSync(OUTPUT_DIR).filter((f) =>
		f.match(/^benchmarks-chunk-\d+\.ts$/),
	);
	for (const f of existingChunks) {
		unlinkSync(join(OUTPUT_DIR, f));
		console.log(`  🗑️  Deleted old chunk: ${sanitizeForLog(f)}`);
	}

	// Write new chunk files
	let chunkIndex = 0;
	for (let i = 0; i < allEntries.length; i += CHUNK_SIZE) {
		const chunk = allEntries.slice(i, i + CHUNK_SIZE);
		const firstModel = normalizeModelName(scoredModels[i]!.name);
		const lastModel = normalizeModelName(
			scoredModels[Math.min(i + CHUNK_SIZE, allEntries.length) - 1]!.name,
		);

		const content = generateChunkFile(
			chunkIndex,
			chunk,
			firstModel,
			lastModel,
			today,
		);

		const filename = `benchmarks-chunk-${chunkIndex}.ts`;
		const entryCount: number = chunk.length;
		writeFileSync(join(OUTPUT_DIR, filename), content, "utf-8");
		console.log(
			`  ✅ Wrote ${filename} (${sanitizeForLog(entryCount)} models: ${sanitizeForLog(firstModel)} .. ${sanitizeForLog(lastModel)})`,
		);
		chunkIndex++;
	}

	// Auto-update hardcoded-benchmarks.ts to match the new chunk count
	const mainFile = join(OUTPUT_DIR, "hardcoded-benchmarks.ts");
	const mainContent = readFileSync(mainFile, "utf-8");
	const currentChunks = new Set(
		Array.from(mainContent.matchAll(/BENCHMARKS_CHUNK_(\d+)/g), (m) => m[1]),
	).size;
	if (currentChunks !== chunkIndex) {
		console.log(
			`\n🔄 Updating hardcoded-benchmarks.ts: ${currentChunks} chunks → ${chunkIndex} chunks`,
		);

		// Generate new import lines
		const chunkImports: string[] = [];
		for (let i = 0; i < chunkIndex; i++) {
			chunkImports.push(
				`import { BENCHMARKS_CHUNK_${i} } from "./benchmarks-chunk-${i}.ts";`,
			);
		}
		const newImportSection = chunkImports.join("\n");

		// Generate new spread lines for the export
		const chunkSpreads: string[] = [];
		for (let i = 0; i < chunkIndex; i++) {
			chunkSpreads.push(`\t...BENCHMARKS_CHUNK_${i},`);
		}
		const newSpreadSection = chunkSpreads.join("\n");

		// Replace import block (string-based, no ReDoS-prone regex)
		const importStart = mainContent.indexOf("import { BENCHMARKS_CHUNK_0");
		const importEnd = mainContent.indexOf("\nexport interface");
		let updatedContent = mainContent;
		if (importStart !== -1 && importEnd !== -1) {
			updatedContent =
				mainContent.slice(0, importStart) +
				newImportSection +
				mainContent.slice(importEnd);
		}

		// Replace spread block (string-based, no ReDoS-prone regex)
		const spreadStart = updatedContent.indexOf("\t...BENCHMARKS_CHUNK_0,");
		const spreadEnd = updatedContent.indexOf("\t};");
		const finalContent =
			spreadStart !== -1 && spreadEnd !== -1
				? updatedContent.slice(0, spreadStart) +
					newSpreadSection +
					"\n" +
					updatedContent.slice(spreadEnd)
				: updatedContent;

		writeFileSync(mainFile, finalContent, "utf-8");
		console.log(
			`  ✅ Updated hardcoded-benchmarks.ts with ${chunkIndex} chunk imports`,
		);
	}

	console.log(`\n✅ Generated ${chunkIndex} chunk files in ${OUTPUT_DIR}/`);
}

async function main() {
	try {
		console.log("🔄 Benchmark Data Updater\n");

		const models = await fetchAIData();
		generateBenchmarksChunks(models);

		console.log("\n📝 Next steps:");
		console.log("  1. Review the chunk file changes");
		console.log(
			"  2. Verify hardcoded-benchmarks.ts imports — auto-updated if chunk count changed",
		);
		console.log("  3. Run tests: npm run test:run");
		console.log("  4. Commit and push");
		console.log("  5. Create PR if this was an automated update");
	} catch (error) {
		const message =
			error instanceof Error ? error.message : "An unknown error occurred";
		console.error("\n❌ Error:", sanitizeForLog(message));
		process.exit(1);
	}
}

// Top-level await — SonarCloud S7785
main().catch((err: unknown) => {
	const message =
		err instanceof Error ? err.message : "An unknown error occurred";
	console.error("Fatal:", sanitizeForLog(message));
	process.exit(1);
});
