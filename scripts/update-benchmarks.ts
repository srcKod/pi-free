/**
 * Script to update hardcoded benchmark data
 * Run: ARTIFICIAL_ANALYSIS_API_KEY=xxx npx tsx scripts/update-benchmarks.ts
 *
 * This fetches fresh data from Artificial Analysis API and updates
 * provider-failover/benchmarks-chunk-*.ts files.
 *
 * The main hardcoded-benchmarks.ts is a hand-maintained aggregator that
 * imports and merges all chunks — this script does NOT overwrite it.
 */

import { readdirSync, unlinkSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const API_KEY = process.env.ARTIFICIAL_ANALYSIS_API_KEY;

if (!API_KEY) {
	console.error(
		"❌ Error: ARTIFICIAL_ANALYSIS_API_KEY environment variable required",
	);
	console.error("Get a free key at: https://artificialanalysis.ai");
	process.exit(1);
}

const CHUNK_SIZE = 90; // Models per chunk file (~2000 lines each)
const OUTPUT_DIR = join(process.cwd(), "provider-failover");

interface AAModel {
	id: string;
	name: string;
	slug: string;
	model_creator: {
		id: string;
		name: string;
		slug: string;
	};
	release_date: string;
	evaluations: {
		artificial_analysis_intelligence_index: number | null;
		artificial_analysis_coding_index: number | null;
		artificial_analysis_math_index: number | null;
		mmlu_pro: number | null;
		gpqa: number | null;
		hle: number | null;
	};
	context_window?: number;
	supports_reasoning?: boolean;
	supports_vision?: boolean;
}

async function fetchAIData(): Promise<AAModel[]> {
	console.log("📡 Fetching data from Artificial Analysis API...");

	const response = await fetch(
		"https://artificialanalysis.ai/api/v2/data/llms/models",
		{
			headers: {
				"x-api-key": API_KEY,
				Accept: "application/json",
			},
		},
	);

	if (!response.ok) {
		throw new Error(`API error: ${response.status} ${response.statusText}`);
	}

	const rawData = (await response.json()) as unknown;

	// API returns { data: [...] } or direct array
	let models: AAModel[];
	if (Array.isArray(rawData)) {
		models = rawData as AAModel[];
	} else if (rawData && typeof rawData === "object") {
		const obj = rawData as Record<string, unknown>;
		models = (obj.data || obj.models || []) as AAModel[];
	} else {
		models = [];
	}

	if (!Array.isArray(models) || models.length === 0) {
		console.error("Unexpected API response structure");
		throw new Error("API response did not contain models array");
	}

	return models;
}

function normalizeModelName(name: string): string {
	return name
		.toLowerCase()
		.replace(/[^-a-z0-9.]+/g, "-")
		.replace(/^-+/, "")
		.replace(/-+$/, "");
}

function generateChunkFile(
	chunkIndex: number,
	entries: string[],
	firstModel: string,
	lastModel: string,
	today: string,
): string {
	return `// Auto-generated benchmark data chunk ${chunkIndex}
// Models: ${firstModel} .. ${lastModel} (${entries.length} entries)
// Last updated: ${today}
// DO NOT EDIT MANUALLY — generated by scripts/update-benchmarks.ts

import type { HardcodedBenchmark } from "./hardcoded-benchmarks.ts";

export const BENCHMARKS_CHUNK_${chunkIndex}: Record<string, HardcodedBenchmark> = {
${entries.join("\n")}
};
`;
}

function generateBenchmarksChunks(models: AAModel[]): void {
	const today = new Date().toISOString().split("T")[0];

	// Debug: log first model to see structure
	if (models.length > 0) {
		console.log(
			"Sample model:",
			JSON.stringify(models[0], null, 2).slice(0, 500),
		);
	}

	// Filter to models with intelligence scores (allow 0, reject null/undefined)
	const scoredModels = models.filter(
		(m) => m.evaluations?.artificial_analysis_intelligence_index != null,
	);

	console.log(
		`✅ Found ${scoredModels.length}/${models.length} models with benchmark scores`,
	);

	// Generate entries
	const allEntries = scoredModels.map((model) => {
		const key = normalizeModelName(model.name);
		const e = model.evaluations;
		const score = e.artificial_analysis_intelligence_index!;
		const normalized = Math.round((score / 70) * 100);

		// Helper to format optional number fields
		const fmt = (v: number | null | undefined): string => {
			if (v === null || v === undefined) return "undefined";
			return v.toFixed(3); // Keep 3 decimals for precision
		};

		return `	"${key}": {
		// AA Intelligence Index (composite score)
		intelligenceIndex: ${score.toFixed(1)},
		normalizedScore: ${normalized},

		// AA specific benchmarks
		codingIndex: ${fmt(e.artificial_analysis_coding_index)},
		mathIndex: ${fmt(e.artificial_analysis_math_index)},

		// Academic benchmarks
		mmluPro: ${fmt(e.mmlu_pro)},
		gpqa: ${fmt(e.gpqa)},
		hle: ${fmt(e.hle)},

		// Capabilities
		contextWindow: ${model.context_window || 8192},
		supportsReasoning: ${model.supports_reasoning || false},
		supportsVision: ${model.supports_vision || false},

		// Metadata
		lastUpdated: "${today}",
	},`;
	});

	// Delete old chunk files
	const existingChunks = readdirSync(OUTPUT_DIR).filter((f) =>
		f.match(/^benchmarks-chunk-\d+\.ts$/),
	);
	for (const f of existingChunks) {
		unlinkSync(join(OUTPUT_DIR, f));
		console.log(`  🗑️  Deleted old chunk: ${f}`);
	}

	// Write new chunk files
	let chunkIndex = 0;
	for (let i = 0; i < allEntries.length; i += CHUNK_SIZE) {
		const chunk = allEntries.slice(i, i + CHUNK_SIZE);
		const firstModel = normalizeModelName(scoredModels[i]!.name);
		const lastModel = normalizeModelName(
			scoredModels[Math.min(i + CHUNK_SIZE, allEntries.length) - 1]!.name,
		);

		const content = generateChunkFile(
			chunkIndex,
			chunk,
			firstModel,
			lastModel,
			today,
		);

		const filename = `benchmarks-chunk-${chunkIndex}.ts`;
		writeFileSync(join(OUTPUT_DIR, filename), content, "utf-8");
		console.log(
			`  ✅ Wrote ${filename} (${chunk.length} models: ${firstModel} .. ${lastModel})`,
		);
		chunkIndex++;
	}

	// Check if hardcoded-benchmarks.ts needs updating (new chunk count)
	const mainFile = join(OUTPUT_DIR, "hardcoded-benchmarks.ts");
	const mainContent = require("fs").readFileSync(mainFile, "utf-8");
	const currentChunks = (mainContent.match(/BENCHMARKS_CHUNK_\d+/g) || [])
		.length;
	if (currentChunks !== chunkIndex) {
		console.log(
			`\n⚠️  hardcoded-benchmarks.ts references ${currentChunks} chunks but ${chunkIndex} were generated.`,
		);
		console.log(
			"   Update the imports and spread in hardcoded-benchmarks.ts to match.",
		);
	}

	console.log(`\n✅ Generated ${chunkIndex} chunk files in ${OUTPUT_DIR}/`);
}

async function main() {
	try {
		console.log("🔄 Benchmark Data Updater\n");

		const models = await fetchAIData();
		generateBenchmarksChunks(models);

		console.log("\n📝 Next steps:");
		console.log("  1. Review the chunk file changes");
		console.log(
			"  2. If chunk count changed, update hardcoded-benchmarks.ts imports",
		);
		console.log("  3. Run tests: npm run test:run");
		console.log("  4. Commit and push");
		console.log("  5. Create PR if this was an automated update");
	} catch (error) {
		console.error("\n❌ Error:", error);
		process.exit(1);
	}
}

main();
